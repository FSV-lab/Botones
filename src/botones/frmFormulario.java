    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        //volvemos a crear 4 variables con los campos 
        String a = txtnombre.getText();
        String b = txtapellido.getText();
        String c = txtcorreo.getText();
        String d = txtcelular.getText();//entero int 
        
        int e = Integer.parseInt(d);
        Botones.guardar(a, b, c, e);
        //Botones es el nombre de la clase principal
        //Guarda es el metodo que hemos creado
        //vamos a limpiar datos dentro del boton guardar
        //para no crear un boton nuevo
        txtnombre.setText(" ");
        txtapellido.setText(" ");
        txtcorreo.setText(" ");
        txtcelular.setText( " "); 
        
        
                
        
    }//GEN-LAST:event_btnguardarActionPerformed

